    fn test_compose_fusion_execution_equivalence() {
        let path = std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR"))
            .join("../../examples/compose_decay.opt");
        let src = std::fs::read_to_string(&path).expect("read compose_decay.opt");
        let prog = optic_syntax::parse(&src, optic_syntax::SourceId(1)).expect("parse");
        let hir = optic_hir::lower(prog).expect("lower");
        let typed = optic_typeck::check(hir).expect("typeck");
        let cg = optic_cgir::build(&typed).expect("cgir");
        let pre = optic_opt::optimize_without_compose_fusion(cg.clone()).expect("pre-opt");
        let post = optic_opt::optimize(cg).expect("post-opt");
        assert!(
            post.graph
                .roots
                .first()
                .and_then(|&r| post.graph.nodes.get(r as usize))
                .is_some_and(|n| matches!(
                    n,
                    CgirNode::FusedLoop {
                        compose_body: Some(_),
                        ..
                    }
                )),
            "post-fusion root must be FusedLoop with compose_body"
        );
        let pre_rs = emit(&pre, "optic_runtime").expect("pre emit");
        let post_rs = emit(&post.graph, "optic_runtime").expect("post emit");
        assert!(
            pre_rs.contains("let _c0 =") && pre_rs.contains("let _c1 ="),
            "unfused compose must use chain temps"
        );
        assert!(
            post_rs.contains("optic(fused):"),
            "fused compose must carry provenance comment"
        );
        let pre_after = run_emitted_and_capture_after(&pre_rs);
        let post_after = run_emitted_and_capture_after(&post_rs);
        assert_eq!(
            pre_after, post_after,
            "compose fusion must preserve mutations (pre={pre_after}, post={post_after})"
        );
        assert!(
            pre_after.contains("95.0") && pre_after.contains("75.0") && pre_after.contains("45.0"),
            "unexpected mutations: {pre_after}"
        );
    }

    fn run_emitted_and_capture_after(emitted: &str) -> String {
        let tmp = tempfile::tempdir().expect("tempdir");
        let runtime = std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../optic-runtime");
        std::fs::write(
            tmp.path().join("Cargo.toml"),
            format!(
                "[package]\nname=\"v\"\nversion=\"0.1.0\"\nedition=\"2021\"\n[dependencies]\noptic-runtime = {{ path = \"{}\" }}\n[[bin]]\nname=\"v\"\npath=\"main.rs\"\n",
                runtime.display()
            ),
        )
        .expect("manifest");
        std::fs::write(tmp.path().join("main.rs"), emitted).expect("main.rs");
        let out = std::process::Command::new("cargo")
            .args([
                "run",
                "--quiet",
                "--manifest-path",
                &tmp.path().join("Cargo.toml").to_string_lossy(),
            ])
            .current_dir(tmp.path())
            .output()
            .expect("cargo run");
        assert!(
            out.status.success(),
            "run failed: {}",
            String::from_utf8_lossy(&out.stderr)
        );
        let stdout = String::from_utf8_lossy(&out.stdout);
        stdout
            .lines()
            .find(|l| l.starts_with("after:"))
            .unwrap_or_else(|| panic!("missing after line in {stdout}"))
            .to_string()
    }

