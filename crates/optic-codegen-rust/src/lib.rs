    fn pipeline_to_emitted(path: &std::path::Path) -> Result<(String, String), String> {
        let src = std::fs::read_to_string(path).map_err(|e| e.to_string())?;
        let prog =
            optic_syntax::parse(&src, optic_syntax::SourceId(1)).map_err(|e| format!("{e:?}"))?;
        let hir = optic_hir::lower(prog).map_err(|e| format!("{e:?}"))?;
        let typed = optic_typeck::check(hir).map_err(|e| format!("{e:?}"))?;
        let cg = optic_cgir::build(&typed).map_err(|e| format!("{e:?}"))?;
        let pre = optic_opt::optimize_without_compose_fusion(cg.clone())?;
        let post = optic_opt::optimize(cg).map_err(|d| d.rule)?; // move not clone; more detail if needed via rule (Issue 3)
        let pre_rs = emit(&pre, "optic_runtime").map_err(|e| e.to_string())?;
        let post_rs = emit(&post.graph, "optic_runtime").map_err(|e| e.to_string())?;
        Ok((pre_rs, post_rs))
    }

