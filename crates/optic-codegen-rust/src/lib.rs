    fn parse_after_entities(stdout: &str) -> Vec<f32> {
        // reuse structured [..] split pattern from cli execution tests for exact (not heuristic) (Issue 3)
        if let Some(line) = stdout.lines().find(|l| l.starts_with("after:")) {
            if let (Some(start), Some(end)) = (line.find('['), line.find(']')) {
                return line[start + 1..end]
                    .split(',')
                    .map(|s| s.trim().parse().unwrap_or(0.0))
                    .collect();
            }
        }
        vec![]
    }

    fn trusted_tool_path_sim() -> String {
        // hoisted for use by both harness and parity test (avoids verbatim dupe in test) (Issue 2)
        const TRUSTED_TOOL_DIRS: &[&str] = &["/usr/bin", "/bin"];
        fn locate_tool_bin_sim(program: &str) -> Option<std::path::PathBuf> {
            for dir in TRUSTED_TOOL_DIRS {
                let candidate = std::path::Path::new(dir).join(program);
                if candidate.is_file() {
                    return Some(candidate);
                }
            }
            if let Some(rustup_home) = option_env!("RUSTUP_HOME") {
                let toolchains = std::path::Path::new(rustup_home).join("toolchains");
                if let Ok(entries) = std::fs::read_dir(&toolchains) {
                    for entry in entries.flatten() {
                        let candidate = entry.path().join("bin").join(program);
                        if candidate.is_file() {
                            return Some(candidate);
                        }
                    }
                }
            }
            None
        }
        let mut dirs: Vec<String> = TRUSTED_TOOL_DIRS.iter().map(|s| s.to_string()).collect();
        for name in ["cargo", "rustc"] {
            if let Some(path) = locate_tool_bin_sim(name) {
                if let Some(parent) = path.parent() {
                    let s = parent.to_string_lossy().to_string();
                    if !dirs.contains(&s) {
                        dirs.insert(0, s);
                    }
                }
            }
        }
        dirs.join(":")
    }

    fn run_emitted_and_capture_after(emitted: &str) -> Result<String, String> {
        let tmp = tempfile::tempdir().map_err(|e| format!("tempdir: {e}"))?;
        // NOTE: parse_after_entities + some contains for nested/record; prefer structured parsers from cli/tests/execution.rs (parse_entities_line, parse_tag_values_line, parse_transform_*) for future-proofing vs Debug output.
        // align with validated_runtime_crate_path (canonicalize + workspace escape guard + lib check) for trust boundary (Issue A)
        let runtime_rel =
            std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../optic-runtime");
        let lib = runtime_rel.join("src/lib.rs");
        if !lib.exists() {
            return Err(format!(
                "optic-runtime not found at {}",
                runtime_rel.display()
            ));
        }
        let canonical = std::fs::canonicalize(&runtime_rel)
            .map_err(|e| format!("canonicalize runtime: {e}"))?;
        let ws = std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR"))
            .join("..")
            .join("..");
        let ws_can = std::fs::canonicalize(&ws).map_err(|e| format!("ws canonical: {e}"))?;
        if !canonical.starts_with(&ws_can) {
            return Err("runtime path escapes workspace".to_string());
        }
        let runtime = canonical;
        std::fs::write(
            tmp.path().join("Cargo.toml"),
            format!(
                "[package]\nname=\"v\"\nversion=\"0.1.0\"\nedition=\"2021\"\n[dependencies]\noptic-runtime = {{ path = \"{}\" }}\n[[bin]]\nname=\"v\"\npath=\"main.rs\"\n",
                runtime.display()
            ),
        )
        .map_err(|e| format!("manifest: {e}"))?;
        std::fs::write(tmp.path().join("main.rs"), emitted).map_err(|e| format!("main.rs: {e}"))?;
        // align to CLI sandbox_command behaviorally (env_clear + trusted PATH + full homes); replication of locate/scan for parity (Issue A / review nit on remaining test<->cli dupe; doctor deduped via reuse)
        let cargo_home = tmp.path().join("cargo-home");
        let rustup_home = tmp.path().join("rustup-home");
        std::fs::create_dir_all(&cargo_home).map_err(|e| format!("cargo_home dir: {e}"))?;
        std::fs::create_dir_all(&rustup_home).map_err(|e| format!("rustup_home dir: {e}"))?;
        let cargo_bin: &str = env!("CARGO");
        let path = trusted_tool_path_sim();
        // Dupe of cli sandbox env setup left (per smallest + Issue 11); parity test exists.
        let out = std::process::Command::new(cargo_bin)
            .env_clear()
            .env("PATH", path)
            .env("HOME", tmp.path())
            .env("CARGO_HOME", &cargo_home)
            .env("RUSTUP_HOME", &rustup_home)
            .env("RUSTUP_TOOLCHAIN", "stable")
            .args([
                "run",
                "--quiet",
                "--manifest-path",
                &tmp.path().join("Cargo.toml").to_string_lossy(),
            ])
            .current_dir(tmp.path())
            .output()
            .map_err(|e| format!("cargo run: {e}"))?;
        if !out.status.success() {
            return Err(format!(
                "run failed: {}",
                String::from_utf8_lossy(&out.stderr)
            ));
        }
        let stdout = String::from_utf8_lossy(&out.stdout);
        Ok(stdout
            .lines()
            .find(|l| l.starts_with("after:"))
            .ok_or_else(|| format!("missing after line in {stdout}"))?
            .to_string())
    }

