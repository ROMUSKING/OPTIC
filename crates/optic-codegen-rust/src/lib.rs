//! optic-codegen-rust — emit exact ch. 11 loop shapes from (post-fusion) CGIR (M5).

use optic_cgir::{compose_leaf_chain, CgirGraph, CgirNode, NodeId};
use optic_hir::{HirExpr, RegionMap};

enum QueryMode {
    MapDecay { root: NodeId },
    MapProduct { root: NodeId },
    MapCompose { root: NodeId, compose_id: NodeId },
    FusedCompose { root: NodeId },
    Get { root: NodeId },
    Set { root: NodeId },
}

const RUST_KEYWORDS: &[&str] = &[
    "as", "async", "await", "break", "const", "continue", "crate", "dyn", "else", "enum", "extern",
    "false", "fn", "for", "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub",
    "ref", "return", "self", "Self", "static", "struct", "super", "trait", "true", "type",
    "unsafe", "use", "where", "while",
];

/// Reject user-derived names that would emit invalid Rust identifiers.
fn is_valid_rust_ident(name: &str) -> bool {
    let mut chars = name.chars();
    let Some(first) = chars.next() else {
        return false;
    };
    if !first.is_ascii_alphabetic() && first != '_' {
        return false;
    }
    if !chars.all(|c| c.is_ascii_alphanumeric() || c == '_') {
        return false;
    }
    !RUST_KEYWORDS.contains(&name)
}

fn validate_user_ident(name: &str, context: &str) -> Result<(), String> {
    if is_valid_rust_ident(name) {
        Ok(())
    } else if RUST_KEYWORDS.contains(&name) {
        Err(format!("reserved Rust keyword for {context}: {name:?}"))
    } else {
        Err(format!("invalid Rust identifier for {context}: {name:?}"))
    }
}

pub fn emit(graph: &CgirGraph, _runtime: &str) -> Result<String, String> {
    let mut out = String::with_capacity(1024);
    out.push_str("// AUTO-GENERATED by opticc (syntax->hir->typeck->cgir->opt->codegen)\n");
    out.push_str("use optic_runtime::Cursor;\n\n");

    let query_mode = detect_query_mode(graph)?;
    let struct_name = costate_struct_name(graph);
    let leaf_nodes: Vec<_> = graph
        .nodes
        .iter()
        .filter_map(|n| {
            if let CgirNode::OpticLeaf { name, summary, .. } = n {
                Some((name.clone(), summary))
            } else {
                None
            }
        })
        .collect();

    let mut all_regions: Vec<String> = vec![];
    let mut leaf_names: Vec<String> = vec![];
    let mut seen = std::collections::HashSet::new();
    for (nm, sum) in &leaf_nodes {
        validate_user_ident(nm, "optic name")?;
        leaf_names.push(nm.clone());
        for r in sum
            .get_reads
            .iter()
            .chain(&sum.put_writes)
            .chain(&sum.put_reads)
        {
            if seen.insert(r.clone()) {
                all_regions.push(r.clone());
            }
        }
    }

    let est = 1024 + leaf_names.len() * 128 + all_regions.len() * 64;
    out.reserve(est.max(4096));

    emit_record_types(&mut out, &graph.region_map, &struct_name)?;
    out.push_str(&format!("#[derive(Debug)]\npub struct {struct_name} {{\n"));
    if graph.region_map.columns.is_empty() {
        return Err("codegen: region_map has no columns from data decl".into());
    }
    for (col_name, info) in &graph.region_map.columns {
        validate_user_ident(col_name, "region field")?;
        optic_hir::validate_rust_type(&info.rust_ty)?;
        out.push_str(&format!("    pub {}: {},\n", col_name, info.rust_ty));
    }
    out.push_str("}\n\n");

    match &query_mode {
        QueryMode::Get { root } => {
            let optic_name = query_optic_name(graph, *root)?;
            let field = primary_field_for_optic(graph, &optic_name)?;
            out.push_str(&format!(
                "pub fn run_example(entities: &mut {struct_name}) {{\n"
            ));
            out.push_str(&format!("    let n = entities.{}.len();\n", field));
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str(&format!(
                "        let _v = cursor_0.arena.{}[cursor_0.id];\n",
                field
            ));
            out.push_str("        println!(\"get: {}\", _v);\n");
            out.push_str("    }\n}\n\n");
        }
        QueryMode::Set { root } => {
            let CgirNode::QuerySet {
                optic_name,
                value_repr,
                ..
            } = &graph.nodes[*root as usize]
            else {
                return Err(format!(
                    "invalid set root: expected QuerySet at node {root}"
                ));
            };
            let field = primary_field_for_optic(graph, optic_name)?;
            let lit = validate_f32_literal(value_repr)?;
            out.push_str(&format!(
                "pub fn run_example(entities: &mut {struct_name}) {{\n"
            ));
            out.push_str(&format!("    let n = entities.{}.len();\n", field));
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str(&format!(
                "        cursor_0.arena.{}[cursor_0.id] = {lit};\n",
                field
            ));
            out.push_str("    }\n}\n\n");
        }
        QueryMode::MapProduct { root } => {
            let CgirNode::QueryMap {
                optic_name,
                map_body,
                map_param,
                ..
            } = &graph.nodes[*root as usize]
            else {
                return Err("invalid map root".into());
            };
            let regions = product_regions_ordered(graph, optic_name)?;
            let comment_names = if leaf_names.is_empty() {
                "unknown".to_string()
            } else {
                leaf_names.join(", ")
            };
            out.push_str(&format!("// optic(fused): [{}]\n", comment_names));
            let n_field = regions
                .first()
                .map(|r| lookup_region_field(&graph.region_map, r))
                .transpose()?
                .ok_or_else(|| "product map requires at least one region".to_string())?;
            out.push_str(&format!(
                "pub fn run_example(entities: &mut {struct_name}) {{\n"
            ));
            out.push_str(&format!("    let n = entities.{n_field}.len();\n"));
            for reg in regions.iter().skip(1) {
                let f = lookup_region_field(&graph.region_map, reg)?;
                out.push_str(&format!("    assert_eq!(n, entities.{f}.len());\n"));
            }
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            for reg in &regions {
                let f = lookup_region_field(&graph.region_map, reg)?;
                let bind = lookup_region_bind(&graph.region_map, reg)?;
                out.push_str(&format!(
                    "        let {bind} = cursor_0.arena.{f}[cursor_0.id];\n"
                ));
            }
            let binds: Vec<String> = regions
                .iter()
                .map(|r| lookup_region_bind(&graph.region_map, r))
                .collect::<Result<_, _>>()?;
            for p in map_param
                .split(',')
                .map(str::trim)
                .filter(|s| !s.is_empty())
            {
                validate_user_ident(p, "map parameter")?;
            }
            let body_rust = emit_map_body_product(map_body.as_ref(), map_param, &binds)?;
            emit_product_stores(&mut out, graph, &regions, &body_rust, map_body.as_ref())?;
            out.push_str("    }\n}\n\n");
        }
        QueryMode::MapDecay { root } => {
            let CgirNode::QueryMap {
                map_body,
                map_param,
                ..
            } = &graph.nodes[*root as usize]
            else {
                return Err("invalid map root".into());
            };
            let field = all_regions
                .first()
                .map(|r| lookup_region_field(&graph.region_map, r))
                .transpose()?
                .ok_or_else(|| "map decay requires at least one read region".to_string())?;
            let bind = lookup_region_bind(
                &graph.region_map,
                all_regions.first().expect("non-empty all_regions"),
            )?;
            out.push_str(&format!(
                "pub fn run_example(entities: &mut {struct_name}) {{\n"
            ));
            out.push_str(&format!("    let n = entities.{field}.len();\n"));
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str(&format!(
                "        let {bind} = cursor_0.arena.{field}[cursor_0.id];\n"
            ));
            validate_user_ident(map_param, "map parameter")?;
            let body_rust = emit_map_body(map_body.as_ref(), map_param, &bind)?;
            out.push_str(&format!(
                "        let _new = {body_rust};\n        cursor_0.arena.{field}[cursor_0.id] = _new;\n"
            ));
            out.push_str("    }\n}\n\n");
        }
        QueryMode::MapCompose { root, compose_id } => {
            emit_compose_map(&mut out, graph, *root, *compose_id, &struct_name)?;
        }
        QueryMode::FusedCompose { root } => {
            emit_fused_compose_loop(&mut out, graph, *root, &leaf_names, &struct_name)?;
        }
    }

    out.push_str(&format!(
        "fn main() {{\n    let mut world = {struct_name} {{ "
    ));
    let mut inits = vec![];
    if graph.region_map.columns.is_empty() {
        return Err("codegen: region_map has no columns from data decl".into());
    }
    for (col, info) in &graph.region_map.columns {
        let init = column_init_value(&graph.region_map, col, info.element_ty.as_deref())?;
        inits.push(format!("{col}: {init}"));
    }
    if inits.is_empty() {
        return Err("codegen: cannot synthesize main() initializer without data columns".into());
    }
    out.push_str(&inits.join(", "));
    out.push_str(" };\n");
    out.push_str("    println!(\"before: {:?}\", world);\n");
    out.push_str("    run_example(&mut world);\n");
    out.push_str("    println!(\"after:  {:?}\", world);\n");
    out.push_str("}\n");
    Ok(out)
}

fn emit_product_stores(
    out: &mut String,
    graph: &CgirGraph,
    regions: &[String],
    body_rust: &str,
    map_body: &HirExpr,
) -> Result<(), String> {
    let new_names: Vec<String> = regions
        .iter()
        .map(|r| lookup_region_bind(&graph.region_map, r).map(|b| format!("{b}_new")))
        .collect::<Result<_, _>>()?;
    if matches!(map_body, HirExpr::Tuple(elems, _) if elems.len() == regions.len()) {
        out.push_str("        let (");
        out.push_str(&new_names.join(", "));
        out.push_str(") = ");
        out.push_str(body_rust);
        out.push_str(";\n");
        for (reg, new_name) in regions.iter().zip(&new_names) {
            let f = lookup_region_field(&graph.region_map, reg)?;
            out.push_str(&format!(
                "        cursor_0.arena.{f}[cursor_0.id] = {new_name};\n"
            ));
        }
        return Ok(());
    }
    if regions.len() > 1 {
        return Err(format!(
            "product map tuple arity mismatch: {} regions, body {:?}",
            regions.len(),
            map_body
        ));
    } else {
        let field = regions
            .first()
            .map(|r| lookup_region_field(&graph.region_map, r))
            .transpose()?
            .ok_or_else(|| "product store requires a region".to_string())?;
        out.push_str(&format!("        let _new = {body_rust};\n"));
        out.push_str(&format!(
            "        cursor_0.arena.{field}[cursor_0.id] = _new;\n"
        ));
    }
    Ok(())
}

fn query_optic_name(graph: &CgirGraph, root: NodeId) -> Result<String, String> {
    match graph.nodes.get(root as usize) {
        Some(CgirNode::QueryGet { optic_name, .. })
        | Some(CgirNode::QuerySet { optic_name, .. })
        | Some(CgirNode::QueryMap { optic_name, .. }) => Ok(optic_name.clone()),
        _ => Err(format!(
            "invalid query root at node {root}: expected QueryGet/Set/Map"
        )),
    }
}

fn product_regions_ordered(graph: &CgirGraph, optic_name: &str) -> Result<Vec<String>, String> {
    let Some(&nid) = graph.resolved_optics.get(optic_name) else {
        return Err(format!(
            "product map optic `{optic_name}` not resolved in CGIR graph"
        ));
    };
    let regions = collect_regions_from_node(graph, nid);
    if regions.is_empty() {
        return Err(format!(
            "product map optic `{optic_name}` has no regions from product spine"
        ));
    }
    Ok(regions)
}

fn collect_regions_from_node(graph: &CgirGraph, nid: NodeId) -> Vec<String> {
    match graph.nodes.get(nid as usize) {
        Some(CgirNode::ProductFlat { children, .. }) => children
            .iter()
            .flat_map(|&cid| collect_regions_from_node(graph, cid))
            .collect(),
        Some(CgirNode::Product { lhs, rhs, .. }) => {
            let mut out = collect_regions_from_node(graph, *lhs);
            out.extend(collect_regions_from_node(graph, *rhs));
            out
        }
        Some(CgirNode::Compose { lhs, rhs, .. }) => {
            let mut out = collect_regions_from_node(graph, *lhs);
            out.extend(collect_regions_from_node(graph, *rhs));
            out
        }
        Some(CgirNode::OpticLeaf { summary, .. }) => summary.get_reads.clone(),
        _ => vec![],
    }
}

fn emit_map_body(body: &HirExpr, map_param: &str, load_bind: &str) -> Result<String, String> {
    emit_hir_expr_rust(body, map_param, load_bind, None)
}

fn emit_map_body_product(
    body: &HirExpr,
    map_param: &str,
    binds: &[String],
) -> Result<String, String> {
    let load_bind = binds
        .first()
        .map(|s| s.as_str())
        .ok_or_else(|| "product map requires at least one region bind".to_string())?;
    emit_hir_expr_rust(body, map_param, load_bind, Some(binds))
}

fn emit_hir_expr_rust(
    e: &HirExpr,
    map_param: &str,
    load_bind: &str,
    product_binds: Option<&[String]>,
) -> Result<String, String> {
    match e {
        HirExpr::LitInt(n, _) => Ok(format!("{n}.0")),
        HirExpr::LitFloat(f, _) => Ok(if f.fract() == 0.0 {
            format!("{f:.1}")
        } else {
            f.to_string()
        }),
        HirExpr::Var(v, _) => {
            validate_user_ident(v, "map variable")?;
            if let Some(binds) = product_binds {
                let params: Vec<&str> = map_param.split(',').map(str::trim).collect();
                if let Some(i) = params.iter().position(|p| *p == v) {
                    return binds.get(i).cloned().ok_or_else(|| {
                        format!(
                            "product map param `{v}` index {i} out of range ({} binds)",
                            binds.len()
                        )
                    });
                }
            }
            Ok(if v == map_param {
                load_bind.to_string()
            } else {
                v.clone()
            })
        }
        HirExpr::Paren(inner, _) => {
            let inner_rust = emit_hir_expr_rust(inner, map_param, load_bind, product_binds)?;
            Ok(format!("({inner_rust})"))
        }
        HirExpr::Bin {
            op, left, right, ..
        } => {
            let op_s = match op {
                optic_syntax::BinOp::Add => "+",
                optic_syntax::BinOp::Sub => "-",
                optic_syntax::BinOp::Mul => "*",
                optic_syntax::BinOp::Div => "/",
                other => {
                    return Err(format!(
                        "unsupported binary operator in map body: {other:?}"
                    ));
                }
            };
            Ok(format!(
                "{} {} {}",
                emit_hir_expr_rust(left, map_param, load_bind, product_binds)?,
                op_s,
                emit_hir_expr_rust(right, map_param, load_bind, product_binds)?
            ))
        }
        HirExpr::Tuple(elems, _) => {
            let parts: Vec<_> = elems
                .iter()
                .map(|el| emit_hir_expr_rust(el, map_param, load_bind, product_binds))
                .collect::<Result<_, _>>()?;
            Ok(format!("({})", parts.join(", ")))
        }
        HirExpr::TupleProj { base, index, .. } => Ok(format!(
            "{}.{}",
            emit_hir_expr_rust(base, map_param, load_bind, product_binds)?,
            index
        )),
        HirExpr::Unsupported { reason, .. } => Err(format!("unsupported map body: {reason}")),
        other => Err(format!("unsupported HirExpr in map body emit: {other:?}")),
    }
}

fn lookup_region_field(map: &RegionMap, reg: &str) -> Result<String, String> {
    map.region_field(reg)
}

fn lookup_region_bind(map: &RegionMap, reg: &str) -> Result<String, String> {
    map.region_bind(reg)
}

fn emit_record_types(out: &mut String, map: &RegionMap, costate_name: &str) -> Result<(), String> {
    for (type_name, fields) in &map.record_fields {
        if type_name == costate_name {
            continue;
        }
        validate_user_ident(type_name, "record type name")?;
        out.push_str(&format!(
            "#[derive(Debug, Clone, Copy, Default)]\npub struct {type_name} {{\n"
        ));
        for (fname, fty) in fields {
            validate_user_ident(fname, "record field")?;
            optic_hir::validate_rust_type(fty)?;
            out.push_str(&format!("    pub {fname}: {fty},\n"));
        }
        out.push_str("}\n\n");
    }
    Ok(())
}

fn column_init_value(
    map: &RegionMap,
    _col: &str,
    element_ty: Option<&str>,
) -> Result<String, String> {
    // Harness fixture convention: three demo rows for acceptance examples (health_*, position_*).
    match element_ty {
        Some("f32") => Ok("vec![100.0, 80.0, 50.0]".into()),
        Some("i32") | Some("u32") => Ok("vec![0, 0, 0]".into()),
        Some("(f32, f32)") => Ok("vec![(0.0,0.0), (1.0,1.0), (2.0,2.0)]".into()),
        Some(record) if map.record_fields.contains_key(record) => {
            let fields = &map.record_fields[record];
            let rows: Vec<String> = (0..3)
                .map(|i| -> Result<String, String> {
                    let field_inits: Vec<String> = fields
                        .iter()
                        .map(|(fname, fty)| -> Result<String, String> {
                            let v = default_scalar_init_at(fty, i, map)?;
                            Ok(format!("{fname}: {v}"))
                        })
                        .collect::<Result<_, _>>()?;
                    Ok(format!("{record} {{ {} }}", field_inits.join(", ")))
                })
                .collect::<Result<_, _>>()?;
            Ok(format!("vec![{}]", rows.join(", ")))
        }
        Some(ty) => Ok(format!("Vec::<{ty}>::new()")),
        None => Ok("Vec::new()".into()),
    }
}

fn default_scalar_init_at(ty: &str, row: usize, map: &RegionMap) -> Result<String, String> {
    optic_hir::validate_rust_type(ty)?;
    let f = row as f32;
    Ok(match ty {
        "f32" => format!("{f:.1}"),
        "i32" | "u32" => format!("{row}"),
        "(f32, f32)" => format!("({f:.1}, {f:.1})"),
        other if other.starts_with('(') => {
            let n = ty.matches(',').count() + 1;
            format!(
                "({})",
                (0..n)
                    .map(|_| format!("{f:.1}"))
                    .collect::<Vec<_>>()
                    .join(", ")
            )
        }
        other if map.record_fields.contains_key(other) => {
            format!("{other} {{ ..Default::default() }}")
        }
        _ => "Default::default()".into(),
    })
}

fn primary_field_for_optic(graph: &CgirGraph, name: &str) -> Result<String, String> {
    let reg = graph
        .nodes
        .iter()
        .find_map(|n| {
            if let CgirNode::OpticLeaf {
                name: leaf_name,
                summary,
                ..
            } = n
            {
                if leaf_name == name {
                    return summary.get_reads.first().cloned();
                }
            }
            None
        })
        .ok_or_else(|| format!("optic `{name}` has no get_reads region"))?;
    lookup_region_field(&graph.region_map, &reg)
}

fn f32_literal(value: &str) -> String {
    if value.contains('.') {
        value.to_string()
    } else {
        format!("{value}.0")
    }
}

fn validate_f32_literal(value_repr: &str) -> Result<String, String> {
    let trimmed = value_repr.trim();
    if trimmed.is_empty() {
        return Err("empty f32 literal in set value".into());
    }
    if !trimmed
        .chars()
        .all(|c| c.is_ascii_digit() || matches!(c, '.' | '-' | '+'))
    {
        return Err(format!("invalid f32 literal in set value: {trimmed:?}"));
    }
    trimmed
        .parse::<f64>()
        .map_err(|_| format!("invalid f32 literal in set value: {trimmed:?}"))?;
    Ok(f32_literal(trimmed))
}

fn costate_struct_name(graph: &CgirGraph) -> String {
    let raw = graph
        .roots
        .first()
        .and_then(|&r| graph.nodes.get(r as usize))
        .map(|n| match n {
            CgirNode::QueryGet { costate, .. }
            | CgirNode::QuerySet { costate, .. }
            | CgirNode::QueryMap { costate, .. } => costate.clone(),
            CgirNode::FusedLoop { costate, .. } => costate.clone(),
            _ => "entities".into(),
        })
        .unwrap_or_else(|| "entities".into());
    if raw.is_empty() {
        return "Entities".into();
    }
    let mut chars = raw.chars();
    let first = chars.next().unwrap().to_ascii_uppercase().to_string();
    first + chars.as_str()
}

fn validate_leaf_params(leaf: &CgirNode) -> Result<(), String> {
    let CgirNode::OpticLeaf {
        get_param,
        put_state_param,
        put_value_param,
        ..
    } = leaf
    else {
        return Err("validate_leaf_params expects OpticLeaf".into());
    };
    validate_user_ident(get_param, "optic get parameter")?;
    if let Some(s) = put_state_param {
        validate_user_ident(s, "optic put state parameter")?;
    }
    if let Some(v) = put_value_param {
        validate_user_ident(v, "optic put value parameter")?;
    }
    Ok(())
}

/// Structural HirExpr substitution (same pattern as map fusion in optic-opt).
fn substitute_hir_var(e: &HirExpr, name: &str, repl: &str) -> HirExpr {
    match e {
        HirExpr::Var(v, sp) if v == name => HirExpr::Var(repl.into(), *sp),
        HirExpr::Var(v, sp) => HirExpr::Var(v.clone(), *sp),
        HirExpr::LitInt(n, sp) => HirExpr::LitInt(*n, *sp),
        HirExpr::LitFloat(f, sp) => HirExpr::LitFloat(*f, *sp),
        HirExpr::Bin {
            op,
            left,
            right,
            span,
        } => HirExpr::Bin {
            op: *op,
            left: Box::new(substitute_hir_var(left, name, repl)),
            right: Box::new(substitute_hir_var(right, name, repl)),
            span: *span,
        },
        HirExpr::Paren(inner, sp) => {
            HirExpr::Paren(Box::new(substitute_hir_var(inner, name, repl)), *sp)
        }
        HirExpr::Tuple(elems, sp) => HirExpr::Tuple(
            elems
                .iter()
                .map(|el| substitute_hir_var(el, name, repl))
                .collect(),
            *sp,
        ),
        HirExpr::TupleProj { base, index, span } => HirExpr::TupleProj {
            base: Box::new(substitute_hir_var(base, name, repl)),
            index: *index,
            span: *span,
        },
        HirExpr::CursorIndex {
            cursor,
            field,
            span,
        } => HirExpr::CursorIndex {
            cursor: cursor.clone(),
            field: field.clone(),
            span: *span,
        },
        HirExpr::CursorField {
            cursor,
            field,
            span,
        } => HirExpr::CursorField {
            cursor: cursor.clone(),
            field: field.clone(),
            span: *span,
        },
        HirExpr::FocusField { param, path, span } => {
            if param == name {
                HirExpr::FocusField {
                    param: repl.into(),
                    path: path.clone(),
                    span: *span,
                }
            } else {
                HirExpr::FocusField {
                    param: param.clone(),
                    path: path.clone(),
                    span: *span,
                }
            }
        }
        HirExpr::Unsupported { reason, span } => HirExpr::Unsupported {
            reason: reason.clone(),
            span: *span,
        },
    }
}

fn substitute_hir_vars(e: &HirExpr, subs: &[(&str, &str)]) -> HirExpr {
    let mut out = e.clone();
    for (name, repl) in subs {
        out = substitute_hir_var(&out, name, repl);
    }
    out
}

fn emit_pure_hir_expr(e: &HirExpr, cursor: &str) -> Result<String, String> {
    match e {
        HirExpr::LitInt(n, _) => Ok(format!("{n}.0")),
        HirExpr::LitFloat(f, _) => Ok(if f.fract() == 0.0 {
            format!("{f:.1}")
        } else {
            f.to_string()
        }),
        HirExpr::Var(v, _) => {
            validate_user_ident(v, "optic body variable")?;
            Ok(v.clone())
        }
        HirExpr::Paren(inner, _) => {
            let inner_rust = emit_pure_hir_expr(inner, cursor)?;
            Ok(format!("({inner_rust})"))
        }
        HirExpr::Bin {
            op, left, right, ..
        } => {
            let op_s = match op {
                optic_syntax::BinOp::Add => "+",
                optic_syntax::BinOp::Sub => "-",
                optic_syntax::BinOp::Mul => "*",
                optic_syntax::BinOp::Div => "/",
                other => {
                    return Err(format!(
                        "unsupported binary operator in optic body: {other:?}"
                    ));
                }
            };
            Ok(format!(
                "{} {} {}",
                emit_pure_hir_expr(left, cursor)?,
                op_s,
                emit_pure_hir_expr(right, cursor)?
            ))
        }
        HirExpr::CursorIndex { field, .. } => {
            validate_user_ident(field, "region field")?;
            Ok(format!("{cursor}.arena.{field}[{cursor}.id]"))
        }
        HirExpr::CursorField { field, .. } => {
            validate_user_ident(field, "region field")?;
            Ok(format!("{cursor}.arena.{field}"))
        }
        HirExpr::Tuple(elems, _) => {
            let parts: Vec<_> = elems
                .iter()
                .map(|el| emit_pure_hir_expr(el, cursor))
                .collect::<Result<_, _>>()?;
            Ok(format!("({})", parts.join(", ")))
        }
        HirExpr::TupleProj { base, index, .. } => {
            Ok(format!("{}.{}", emit_pure_hir_expr(base, cursor)?, index))
        }
        HirExpr::FocusField { param, path, .. } => {
            if path.is_empty() {
                return Err("empty focus field path in optic body".into());
            }
            Ok(format!("{param}.{}", path.join(".")))
        }
        HirExpr::Unsupported { reason, .. } => Err(format!("unsupported optic body: {reason}")),
    }
}

fn emit_compose_map(
    out: &mut String,
    graph: &CgirGraph,
    root: NodeId,
    compose_id: NodeId,
    struct_name: &str,
) -> Result<(), String> {
    let CgirNode::QueryMap {
        map_body,
        map_param,
        ..
    } = &graph.nodes[root as usize]
    else {
        return Err("invalid compose map root".into());
    };
    // Unfused compose intentionally mirrors fused loop semantics (ch10); equivalence tests rely on this.
    emit_compose_chain_loop(
        out,
        graph,
        compose_id,
        map_body.as_ref(),
        map_param,
        None,
        struct_name,
    )
}

fn emit_fused_compose_loop(
    out: &mut String,
    graph: &CgirGraph,
    root: NodeId,
    leaf_names: &[String],
    struct_name: &str,
) -> Result<(), String> {
    let CgirNode::FusedLoop {
        compose_body,
        original_ids,
        ..
    } = &graph.nodes[root as usize]
    else {
        return Err("invalid fused compose root".into());
    };
    let body = compose_body
        .as_ref()
        .ok_or_else(|| "FusedCompose missing compose_body".to_string())?;
    let names = fused_provenance_names(graph, original_ids, leaf_names);
    // Fused compose shares the same loop body shape as unfused MapCompose (ch10 semantic preservation).
    emit_compose_chain_loop(
        out,
        graph,
        body.compose_id,
        body.map_body.as_ref(),
        &body.map_param,
        Some(&names),
        struct_name,
    )
}

/// Emit get/put spine for a (possibly nested) Compose tree + map body.
fn emit_compose_chain_loop(
    out: &mut String,
    graph: &CgirGraph,
    compose_id: NodeId,
    map_body: &HirExpr,
    map_param: &str,
    fused_names: Option<&str>,
    struct_name: &str,
) -> Result<(), String> {
    let chain = compose_leaf_chain(graph, compose_id)
        .ok_or_else(|| format!("compose {compose_id} is not a leaf spine"))?;
    if chain.is_empty() {
        return Err("compose chain is empty".into());
    }
    let n_field = compose_primary_field(graph, chain[0])?;
    if let Some(names) = fused_names {
        out.push_str(&format!("// optic(fused): [{names}]\n"));
    }
    out.push_str(&format!(
        "pub fn run_example(entities: &mut {struct_name}) {{\n"
    ));
    out.push_str(&format!("    let n = entities.{n_field}.len();\n"));
    out.push_str("    for id_0 in 0..n {\n");
    out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
    validate_user_ident(map_param, "map parameter")?;

    let needs_mut = chain
        .iter()
        .skip(1)
        .any(|&leaf| is_focus_relative_put(graph, leaf));
    let mut binds: Vec<String> = vec![];
    for (i, &leaf) in chain.iter().enumerate() {
        let focus = if i == 0 {
            None
        } else {
            Some(binds[i - 1].as_str())
        };
        let expr = emit_leaf_get(graph, leaf, "cursor_0", focus)?;
        let bind = format!("_c{i}");
        let mut_kw = if needs_mut && i < chain.len() - 1 {
            "mut "
        } else if needs_mut && i == 0 {
            "mut "
        } else {
            ""
        };
        out.push_str(&format!("        let {mut_kw}{bind} = {expr};\n"));
        binds.push(bind);
    }
    let result_bind = binds.last().expect("non-empty chain");
    let body_rust = emit_map_body(map_body, map_param, result_bind)?;
    out.push_str(&format!("        let _updated = {body_rust};\n"));

    let mut value_bind = "_updated".to_string();
    for i in (1..chain.len()).rev() {
        let state_bind = &binds[i - 1];
        if is_focus_relative_put(graph, chain[i]) {
            let field = focus_put_field(graph, chain[i])?;
            out.push_str(&format!("        {state_bind}.{field} = {value_bind};\n"));
            value_bind = state_bind.clone();
        } else {
            let out_bind = format!("_p{i}");
            let expr = emit_leaf_put_value(graph, chain[i], state_bind, &value_bind)?;
            out.push_str(&format!("        let {out_bind} = {expr};\n"));
            emit_leaf_put_stores(graph, chain[i], "cursor_0", &out_bind, out)?;
            value_bind = out_bind;
        }
    }
    emit_leaf_put_stores(graph, chain[0], "cursor_0", &value_bind, out)?;
    out.push_str("    }\n}\n\n");
    Ok(())
}

fn fused_provenance_names(
    graph: &CgirGraph,
    original_ids: &[NodeId],
    fallback: &[String],
) -> String {
    let mut names = vec![];
    for &id in original_ids {
        if let Some(CgirNode::OpticLeaf { name, .. }) = graph.nodes.get(id as usize) {
            if !names.contains(name) {
                names.push(name.clone());
            }
        }
    }
    if names.is_empty() {
        fallback.join(", ")
    } else {
        names.join(", ")
    }
}

fn compose_primary_field(graph: &CgirGraph, entry_leaf: NodeId) -> Result<String, String> {
    let CgirNode::OpticLeaf { summary, .. } = graph
        .nodes
        .get(entry_leaf as usize)
        .ok_or_else(|| format!("compose entry leaf {entry_leaf} missing"))?
    else {
        return Err(format!("compose entry leaf {entry_leaf} not a leaf"));
    };
    let reg = summary
        .get_reads
        .first()
        .ok_or_else(|| "compose lhs has no read region".to_string())?;
    lookup_region_field(&graph.region_map, reg)
}

fn is_focus_relative_put(graph: &CgirGraph, leaf_id: NodeId) -> bool {
    let Some(CgirNode::OpticLeaf { summary, .. }) = graph.nodes.get(leaf_id as usize) else {
        return false;
    };
    summary
        .put_writes
        .iter()
        .any(|r| !graph.region_map.is_top_level_column(r))
}

fn focus_put_field(graph: &CgirGraph, leaf_id: NodeId) -> Result<String, String> {
    let CgirNode::OpticLeaf { summary, .. } = graph
        .nodes
        .get(leaf_id as usize)
        .ok_or_else(|| format!("leaf {leaf_id} missing"))?
    else {
        return Err(format!("node {leaf_id} not OpticLeaf"));
    };
    if summary.put_writes.is_empty() {
        return Err(format!("leaf {leaf_id} has no put_writes"));
    }
    let paths: Vec<String> = summary
        .put_writes
        .iter()
        .map(|reg| {
            let path = graph.region_map.nested_field_path(reg);
            if path.is_empty() {
                reg.clone()
            } else {
                path.join(".")
            }
        })
        .collect();
    if paths.iter().all(|p| p == &paths[0]) {
        Ok(paths[0].clone())
    } else {
        Err(format!(
            "leaf {leaf_id} put_writes disagree on nested assignment path: {paths:?}"
        ))
    }
}

fn emit_leaf_get(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    focus_bind: Option<&str>,
) -> Result<String, String> {
    let leaf = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?;
    validate_leaf_params(leaf)?;
    let CgirNode::OpticLeaf {
        get_body,
        get_param,
        ..
    } = leaf
    else {
        return Err(format!("node {id} not OpticLeaf"));
    };
    let mut body = get_body.as_ref().clone();
    if let Some(bind) = focus_bind {
        body = substitute_hir_var(&body, get_param, bind);
    }
    emit_pure_hir_expr(&body, cursor)
}

fn emit_leaf_put_value(
    graph: &CgirGraph,
    id: NodeId,
    state_bind: &str,
    value_bind: &str,
) -> Result<String, String> {
    let leaf = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?;
    validate_leaf_params(leaf)?;
    let CgirNode::OpticLeaf {
        put_value_body,
        put_state_param,
        put_value_param,
        ..
    } = leaf
    else {
        return Err(format!("node {id} not OpticLeaf"));
    };
    let Some(body) = put_value_body else {
        return Ok(value_bind.to_string());
    };
    let state = put_state_param.as_deref().unwrap_or("x");
    let value = put_value_param.as_deref().unwrap_or("v");
    let subbed = substitute_hir_vars(body.as_ref(), &[(state, state_bind), (value, value_bind)]);
    emit_pure_hir_expr(&subbed, "_unused")
}

fn emit_leaf_put_stores(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    value_bind: &str,
    out: &mut String,
) -> Result<(), String> {
    let CgirNode::OpticLeaf { summary, .. } = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?
    else {
        return Err(format!("node {id} not OpticLeaf"));
    };
    let regions: Vec<_> = if summary.put_writes.is_empty() {
        summary
            .get_reads
            .first()
            .map(|r| vec![r.clone()])
            .unwrap_or_default()
    } else {
        summary.put_writes.clone()
    };
    if regions.is_empty() {
        return Ok(());
    }
    let mut stores = 0usize;
    for reg in &regions {
        if !graph.region_map.is_top_level_column(reg) {
            continue;
        }
        let field = lookup_region_field(&graph.region_map, reg)?;
        validate_user_ident(&field, "region field")?;
        out.push_str(&format!(
            "        {cursor}.arena.{field}[{cursor}.id] = {value_bind};\n"
        ));
        stores += 1;
    }
    if stores == 0 {
        return Err(format!(
            "leaf put_writes {:?} produced no top-level arena stores",
            summary.put_writes
        ));
    }
    Ok(())
}

fn detect_query_mode(graph: &CgirGraph) -> Result<QueryMode, String> {
    let has_product = graph
        .nodes
        .iter()
        .any(|n| matches!(n, CgirNode::Product { .. } | CgirNode::ProductFlat { .. }));

    if graph.roots.is_empty() {
        return Err("CGIR graph has no query root".into());
    }

    for &root in &graph.roots {
        if let Some(node) = graph.nodes.get(root as usize) {
            return Ok(match node {
                CgirNode::FusedLoop { compose_body, .. } if compose_body.is_some() => {
                    QueryMode::FusedCompose { root }
                }
                CgirNode::QueryGet { .. } => QueryMode::Get { root },
                CgirNode::QuerySet { .. } => QueryMode::Set { root },
                CgirNode::QueryMap { optic_name, .. } => {
                    if let Some(&nid) = graph.resolved_optics.get(optic_name) {
                        if matches!(
                            graph.nodes.get(nid as usize),
                            Some(CgirNode::Compose { .. })
                        ) {
                            return Ok(QueryMode::MapCompose {
                                root,
                                compose_id: nid,
                            });
                        }
                    }
                    if has_product {
                        QueryMode::MapProduct { root }
                    } else {
                        QueryMode::MapDecay { root }
                    }
                }
                other => {
                    return Err(format!("unsupported CGIR root node for codegen: {other:?}"));
                }
            });
        }
    }
    Err(format!(
        "CGIR root {:?} is out of bounds (nodes={})",
        graph.roots,
        graph.nodes.len()
    ))
}

