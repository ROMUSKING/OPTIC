//! optic-codegen-rust — emit exact ch. 11 loop shapes from (post-fusion) CGIR (M5).

use optic_cgir::{CgirGraph, CgirNode};

enum QueryMode {
    MapDecay,
    MapProduct,
    Get,
    Set { value: String },
}

pub fn emit(graph: &CgirGraph, _runtime: &str) -> String {
    let mut out = String::with_capacity(1024);
    out.push_str("// AUTO-GENERATED by opticc (syntax->hir->typeck->cgir->opt->codegen)\n");
    out.push_str("use optic_runtime::Cursor;\n\n");

    let leaf_nodes: Vec<_> = graph
        .nodes
        .iter()
        .filter_map(|n| {
            if let CgirNode::OpticLeaf { name, summary, .. } = n {
                Some((name.clone(), summary))
            } else {
                None
            }
        })
        .collect();

    let query_mode = detect_query_mode(graph);
    let is_fused_product = matches!(query_mode, QueryMode::MapProduct)
        || graph
            .nodes
            .iter()
            .any(|n| matches!(n, CgirNode::FusedLoop { .. }))
        || graph
            .nodes
            .iter()
            .any(|n| matches!(n, CgirNode::Product { .. }));

    let mut all_regions: Vec<String> = vec![];
    let mut leaf_names: Vec<String> = vec![];
    let mut seen = std::collections::HashSet::new();
    for (nm, sum) in &leaf_nodes {
        leaf_names.push(nm.clone());
        for r in &sum.get_reads {
            if seen.insert(r.clone()) {
                all_regions.push(r.clone());
            }
        }
        for r in &sum.put_writes {
            if seen.insert(r.clone()) {
                all_regions.push(r.clone());
            }
        }
    }

    let has_pos_leaf = leaf_names
        .iter()
        .any(|n| n.contains("Position") || n.contains("Pos"));
    let est = 1024 + leaf_names.len() * 128 + all_regions.len() * 64;
    out.reserve(est.max(4096));

    out.push_str("#[derive(Debug)]\npub struct Entities {\n");
    for reg in &all_regions {
        if reg.contains("health") {
            out.push_str("    pub healths: Vec<f32>,\n");
        } else if reg.contains("pos") {
            out.push_str("    pub positions: Vec<(f32, f32)>,\n");
        } else {
            out.push_str(&format!("    pub {}: Vec<f32>,\n", reg));
        }
    }
    if all_regions.is_empty() {
        out.push_str("    pub healths: Vec<f32>,\n");
    }
    out.push_str("}\n\n");

    match query_mode {
        QueryMode::Get => {
            out.push_str("pub fn run_example(entities: &mut Entities) {\n");
            out.push_str("    let n = entities.healths.len();\n");
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str("        let _h = cursor_0.arena.healths[cursor_0.id];\n");
            out.push_str("        println!(\"get: {}\", _h);\n");
            out.push_str("    }\n");
            out.push_str("}\n\n");
        }
        QueryMode::Set { value } => {
            let lit = f32_literal(&value);
            out.push_str("pub fn run_example(entities: &mut Entities) {\n");
            out.push_str("    let n = entities.healths.len();\n");
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str(&format!(
                "        cursor_0.arena.healths[cursor_0.id] = {lit};\n"
            ));
            out.push_str("    }\n");
            out.push_str("}\n\n");
        }
        QueryMode::MapProduct if is_fused_product => {
            let comment_names = if leaf_names.is_empty() {
                "unknown".to_string()
            } else {
                leaf_names.join(", ")
            };
            out.push_str(&format!("// optic(fused): [{}]\n", comment_names));
            out.push_str("pub fn run_example(entities: &mut Entities) {\n");
            out.push_str("    let n = entities.healths.len();\n");
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            for reg in &all_regions {
                if reg.contains("health") {
                    out.push_str("        let _h = cursor_0.arena.healths[cursor_0.id];\n");
                } else if reg.contains("pos") {
                    out.push_str("        let _p = cursor_0.arena.positions[cursor_0.id];\n");
                }
            }
            if has_pos_leaf {
                out.push_str("        let _h_new = _h - 1.0;\n");
                out.push_str("        let _p_new = (_p.0 + 0.1, _p.1);\n");
                out.push_str("        cursor_0.arena.healths[cursor_0.id] = _h_new;\n");
                out.push_str("        cursor_0.arena.positions[cursor_0.id] = _p_new;\n");
            } else {
                out.push_str(
                    "        let _h_new = _h - 1.0;\n        cursor_0.arena.healths[cursor_0.id] = _h_new;\n",
                );
            }
            out.push_str("    }\n");
            out.push_str("}\n\n");
        }
        _ => {
            out.push_str("pub fn run_example(entities: &mut Entities) {\n");
            out.push_str("    let n = entities.healths.len();\n");
            out.push_str("    for id_0 in 0..n {\n");
            out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
            out.push_str("        let _h = cursor_0.arena.healths[cursor_0.id];\n");
            out.push_str("        let _h_new = _h - 10.0;\n");
            out.push_str("        cursor_0.arena.healths[cursor_0.id] = _h_new;\n");
            out.push_str("    }\n");
            out.push_str("}\n\n");
        }
    }

    out.push_str("fn main() {\n");
    out.push_str("    let mut world = Entities { ");
    let mut inits = vec!["healths: vec![100.0, 80.0, 50.0]".to_string()];
    if all_regions.iter().any(|r| r.contains("pos")) {
        inits.push("positions: vec![(0.0,0.0), (1.0,1.0), (2.0,2.0)]".into());
    }
    out.push_str(&inits.join(", "));
    out.push_str(" };\n");
    out.push_str("    println!(\"before: {:?}\", world);\n");
    out.push_str("    run_example(&mut world);\n");
    out.push_str("    println!(\"after:  {:?}\", world);\n");
    out.push_str("}\n");
    out
}

fn f32_literal(value: &str) -> String {
    if value.contains('.') {
        value.to_string()
    } else {
        format!("{value}.0")
    }
}

fn detect_query_mode(graph: &CgirGraph) -> QueryMode {
    for &root in &graph.roots {
        if let Some(node) = graph.nodes.get(root as usize) {
            match node {
                CgirNode::QueryGet { .. } => return QueryMode::Get,
                CgirNode::QuerySet { value_repr, .. } => {
                    return QueryMode::Set {
                        value: value_repr.clone(),
                    };
                }
                CgirNode::QueryMap { .. } => {
                    let is_product = graph.nodes.iter().any(|n| {
                        matches!(n, CgirNode::Product { .. } | CgirNode::FusedLoop { .. })
                    });
                    return if is_product {
                        QueryMode::MapProduct
                    } else {
                        QueryMode::MapDecay
                    };
                }
                _ => {}
            }
        }
    }
    QueryMode::MapDecay
}

#[cfg(test)]
mod tests {
    use super::*;
    use optic_cgir::{CgirGraph, CgirNode, FusionProvenance, FusionReason};
    use optic_hir::{ConcreteGrade, Determinism, OpticSummary, OwnershipDim, PathLift, Rational};
    use optic_syntax::Span;
    use std::collections::BTreeMap;
    use std::sync::Arc;

    fn mk_leaf_with_regions(name: &str, regions: Vec<&str>) -> CgirNode {
        let sum = OpticSummary {
            name: Some(name.into()),
            costate: "Entities".into(),
            focus: "f32".into(),
            lift: PathLift::default(),
            get_reads: regions.into_iter().map(|s| s.to_string()).collect(),
            put_reads: vec![],
            put_writes: vec![],
            get_grade: ConcreteGrade {
                cache: 1,
                ownership: OwnershipDim {
                    share: Rational::one(),
                    read_only: false,
                    must_use: false,
                },
            },
            put_grade: ConcreteGrade {
                cache: 1,
                ownership: OwnershipDim {
                    share: Rational::one(),
                    read_only: false,
                    must_use: false,
                },
            },
            get_determinism: Determinism::Pure,
            put_determinism: Determinism::Pure,
            serializable: true,
            provenance: Span::dummy(),
        };
        CgirNode::OpticLeaf {
            id: 0,
            name: name.into(),
            costate: "E".into(),
            focus: "f".into(),
            grade: sum.get_grade.clone(),
            get_fn: "".into(),
            put_fn: "".into(),
            summary: Arc::new(sum),
            provenance: Span::dummy(),
        }
    }

    #[test]
    fn test_emit_capacity_and_regions_from_arc_summaries() {
        let mut nodes = vec![];
        let leaf = mk_leaf_with_regions("H", vec!["healths", "healths", "pos"]);
        nodes.push(leaf);
        let g = CgirGraph {
            nodes,
            roots: vec![],
            provenance_index: BTreeMap::new(),
        };
        let s = emit(&g, "r");
        assert!(s.contains("healths") && s.contains("pos"));
        assert!(s.capacity() >= 1024);
    }
}
