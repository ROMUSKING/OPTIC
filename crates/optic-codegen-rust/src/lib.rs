fn emit_record_types(out: &mut String, map: &RegionMap, costate_name: &str) -> Result<(), String> {
    for (type_name, fields) in &map.record_fields {
        if type_name == costate_name {
            continue;
        }
        validate_user_ident(type_name, "record type name")?;
        out.push_str(&format!(
            "#[derive(Debug, Clone, Copy, Default)]\npub struct {type_name} {{\n"
        ));
        for (fname, fty) in fields {
            validate_user_ident(fname, "record field")?;
            optic_hir::validate_rust_type(fty)?;
            out.push_str(&format!("    pub {fname}: {fty},\n"));
        }
        out.push_str("}\n\n");
    }
    Ok(())
}

fn column_init_value(
    map: &RegionMap,
    _col: &str,
    element_ty: Option<&str>,
) -> Result<String, String> {
    // Harness fixture convention: three demo rows for acceptance examples (health_*, position_*).
    match element_ty {
        Some("f32") => Ok("vec![100.0, 80.0, 50.0]".into()),
        Some("i32") | Some("u32") => Ok("vec![0, 0, 0]".into()),
        Some("(f32, f32)") => Ok("vec![(0.0,0.0), (1.0,1.0), (2.0,2.0)]".into()),
        Some(record) if map.record_fields.contains_key(record) => {
            let fields = &map.record_fields[record];
            let rows: Vec<String> = (0..3)
                .map(|i| -> Result<String, String> {
                    let field_inits: Vec<String> = fields
                        .iter()
                        .map(|(fname, fty)| -> Result<String, String> {
                            let v = default_scalar_init_at(fty, i, map)?;
                            Ok(format!("{fname}: {v}"))
                        })
                        .collect::<Result<_, _>>()?;
                    Ok(format!("{record} {{ {} }}", field_inits.join(", ")))
                })
                .collect::<Result<_, _>>()?;
            Ok(format!("vec![{}]", rows.join(", ")))
        }
        Some(ty) => Ok(format!("Vec::<{ty}>::new()")),
        None => Ok("Vec::new()".into()),
    }
}

fn default_scalar_init_at(ty: &str, row: usize, map: &RegionMap) -> Result<String, String> {
    optic_hir::validate_rust_type(ty)?;
    let f = row as f32;
    Ok(match ty {
        "f32" => format!("{f:.1}"),
        "i32" | "u32" => format!("{row}"),
        "(f32, f32)" => format!("({f:.1}, {f:.1})"),
        other if other.starts_with('(') => {
            let n = ty.matches(',').count() + 1;
            format!(
                "({})",
                (0..n)
                    .map(|_| format!("{f:.1}"))
                    .collect::<Vec<_>>()
                    .join(", ")
            )
        }
        other if map.record_fields.contains_key(other) => {
            format!("{other} {{ ..Default::default() }}")
        }
        _ => "Default::default()".into(),
    })
}

fn primary_field_for_optic(graph: &CgirGraph, name: &str) -> Result<String, String> {
    let Some(&nid) = graph.resolved_optics.get(name) else {
        return Err(format!("optic `{name}` not resolved in CGIR graph"));
    };
    let reg = leaf_summary(graph, nid)?
        .get_reads
        .first()
        .cloned()
        .ok_or_else(|| format!("optic `{name}` has no get_reads region"))?;
    lookup_region_field(&graph.region_map, &reg)
}

fn f32_literal(value: &str) -> String {
    if value.contains('.') {
        value.to_string()
    } else {
        format!("{value}.0")
    }
}

fn validate_f32_literal(value_repr: &str) -> Result<String, String> {
    let trimmed = value_repr.trim();
    if trimmed.is_empty() {
        return Err("empty f32 literal in set value".into());
    }
    if !trimmed
        .chars()
        .all(|c| c.is_ascii_digit() || matches!(c, '.' | '-' | '+'))
    {
        return Err(format!("invalid f32 literal in set value: {trimmed:?}"));
    }
    trimmed
        .parse::<f64>()
        .map_err(|_| format!("invalid f32 literal in set value: {trimmed:?}"))?;
    Ok(f32_literal(trimmed))
}

fn costate_struct_name(graph: &CgirGraph) -> Result<String, String> {
    let raw = graph
        .roots
        .first()
        .and_then(|&r| graph.nodes.get(r as usize))
        .map(|n| match n {
            CgirNode::QueryGet { costate, .. }
            | CgirNode::QuerySet { costate, .. }
            | CgirNode::QueryMap { costate, .. } => costate.clone(),
            CgirNode::FusedLoop { costate, .. } => costate.clone(),
            _ => "entities".into(),
        })
        .unwrap_or_else(|| "entities".into());
    let name = if raw.is_empty() {
        "Entities".into()
    } else {
        let mut chars = raw.chars();
        let first_ch = match chars.next() {
            Some(c) => c,
            None => return Ok("Entities".into()),
        };
        let first = first_ch.to_ascii_uppercase().to_string();
        first + chars.as_str()
    };
    validate_user_ident(&name, "costate struct name")?;
    Ok(name)
}

fn validate_leaf_params(leaf: &CgirNode) -> Result<(), String> {
    match leaf {
        CgirNode::OpticLeaf {
            get_param,
            put_state_param,
            put_value_param,
            ..
        } => {
            validate_user_ident(get_param, "optic get parameter")?;
            if let Some(s) = put_state_param {
                validate_user_ident(s, "optic put state parameter")?;
            }
            if let Some(v) = put_value_param {
                validate_user_ident(v, "optic put value parameter")?;
            }
            Ok(())
        }
        CgirNode::PrismLeaf {
            preview_param,
            review_state_param,
            review_value_param,
            ..
        } => {
            validate_user_ident(preview_param, "optic preview parameter")?;
            if let Some(s) = review_state_param {
                validate_user_ident(s, "optic review state parameter")?;
            }
            if let Some(v) = review_value_param {
                validate_user_ident(v, "optic review value parameter")?;
            }
            Ok(())
        }
        CgirNode::TraversalLeaf {
            get_param,
            set_state_param,
            set_value_param,
            ..
        } => {
            validate_user_ident(get_param, "optic get parameter")?;
            if let Some(s) = set_state_param {
                validate_user_ident(s, "optic set state parameter")?;
            }
            if let Some(v) = set_value_param {
                validate_user_ident(v, "optic set value parameter")?;
            }
            Ok(())
        }
        _ => Err("validate_leaf_params expects OpticLeaf, PrismLeaf, or TraversalLeaf".into()),
    }
}

fn substitute_hir_vars(e: &HirExpr, subs: &[(&str, &str)]) -> HirExpr {
    let mut out = e.clone();
    for (name, repl) in subs {
        out = optic_hir::substitute_hir_ident(&out, name, repl);
    }
    out
}

fn emit_pure_hir_expr(e: &HirExpr, cursor: &str) -> Result<String, String> {
    match e {
        HirExpr::LitInt(n, _) => Ok(format!("{n}.0")),
        HirExpr::LitFloat(f, _) => Ok(if f.fract() == 0.0 {
            format!("{f:.1}")
        } else {
            f.to_string()
        }),
        HirExpr::Var(v, _) => {
            validate_user_ident(v, "optic body variable")?;
            Ok(v.clone())
        }
        HirExpr::Paren(inner, _) => {
            let inner_rust = emit_pure_hir_expr(inner, cursor)?;
            Ok(format!("({inner_rust})"))
        }
        HirExpr::Bin {
            op, left, right, ..
        } => {
            let op_s = match op {
                optic_syntax::BinOp::Add => "+",
                optic_syntax::BinOp::Sub => "-",
                optic_syntax::BinOp::Mul => "*",
                optic_syntax::BinOp::Div => "/",
                other => {
                    return Err(format!(
                        "unsupported binary operator in optic body: {other:?}"
                    ));
                }
            };
            Ok(format!(
                "{} {} {}",
                emit_pure_hir_expr(left, cursor)?,
                op_s,
                emit_pure_hir_expr(right, cursor)?
            ))
        }
        HirExpr::CursorIndex { field, .. } => {
            validate_user_ident(field, "region field")?;
            Ok(format!("{cursor}.arena.{field}[{cursor}.id]"))
        }
        HirExpr::CursorField { field, .. } => {
            validate_user_ident(field, "region field")?;
            Ok(format!("{cursor}.arena.{field}"))
        }
        HirExpr::Tuple(elems, _) => {
            let parts: Vec<_> = elems
                .iter()
                .map(|el| emit_pure_hir_expr(el, cursor))
                .collect::<Result<_, _>>()?;
            Ok(format!("({})", parts.join(", ")))
        }
        HirExpr::TupleProj { base, index, .. } => {
            Ok(format!("{}.{}", emit_pure_hir_expr(base, cursor)?, index))
        }
        HirExpr::FocusField { param, path, .. } => {
            if path.is_empty() {
                return Err("empty focus field path in optic body".into());
            }
            Ok(format!("{param}.{}", path.join(".")))
        }
        HirExpr::Unsupported { reason, .. } => Err(format!("unsupported optic body: {reason}")),
    }
}

fn emit_compose_map(
    out: &mut String,
    graph: &CgirGraph,
    root: NodeId,
    compose_id: NodeId,
    struct_name: &str,
) -> Result<(), String> {
    let CgirNode::QueryMap {
        map_body,
        map_param,
        ..
    } = &graph.nodes[root as usize]
    else {
        return Err("invalid compose map root".into());
    };
    // Unfused compose intentionally mirrors fused loop semantics (ch10); equivalence tests rely on this.
    emit_compose_chain_loop(
        out,
        graph,
        compose_id,
        map_body.as_ref(),
        map_param,
        None,
        struct_name,
    )
}

fn emit_fused_compose_loop(
    out: &mut String,
    graph: &CgirGraph,
    root: NodeId,
    leaf_names: &[String],
    struct_name: &str,
) -> Result<(), String> {
    let CgirNode::FusedLoop {
        compose_body,
        original_ids,
        ..
    } = &graph.nodes[root as usize]
    else {
        return Err("invalid fused compose root".into());
    };
    let body = compose_body
        .as_ref()
        .ok_or_else(|| "FusedCompose missing compose_body".to_string())?;
    let names = fused_provenance_names(graph, original_ids, leaf_names);
    // Fused compose shares the same loop body shape as unfused MapCompose (ch10 semantic preservation).
    emit_compose_chain_loop(
        out,
        graph,
        body.compose_id,
        body.map_body.as_ref(),
        &body.map_param,
        Some(&names),
        struct_name,
    )
}

/// Emit get/put spine for a (possibly nested) Compose tree + map body.
fn emit_compose_chain_loop(
    out: &mut String,
    graph: &CgirGraph,
    compose_id: NodeId,
    map_body: &HirExpr,
    map_param: &str,
    fused_names: Option<&str>,
    struct_name: &str,
) -> Result<(), String> {
    let chain = compose_leaf_chain(graph, compose_id)
        .ok_or_else(|| format!("compose {compose_id} is not a leaf spine"))?;
    if chain.is_empty() {
        return Err("compose chain is empty".into());
    }
    if chain.iter().any(|&leaf| {
        matches!(
            graph.nodes.get(leaf as usize),
            Some(CgirNode::PrismLeaf { .. } | CgirNode::TraversalLeaf { .. })
        )
    }) {
        return Err(
            "compose chain with PrismLeaf or TraversalLeaf is not supported in narrow v0 codegen (use direct query)"
                .into(),
        );
    }
    let n_field = compose_primary_field(graph, chain[0])?;
    if let Some(names) = fused_names {
        out.push_str(&format!("// optic(fused): [{names}]\n"));
    }
    out.push_str(&format!(
        "pub fn run_example(entities: &mut {struct_name}) {{\n"
    ));
    out.push_str(&format!("    let n = entities.{n_field}.len();\n"));
    out.push_str("    for id_0 in 0..n {\n");
    out.push_str("        let cursor_0 = Cursor::new(entities, id_0);\n");
    validate_user_ident(map_param, "map parameter")?;

    let needs_mut = chain
        .iter()
        .skip(1)
        .any(|&leaf| is_focus_relative_put(graph, leaf));
    let mut binds: Vec<String> = vec![];
    for (i, &leaf) in chain.iter().enumerate() {
        let focus = if i == 0 {
            None
        } else {
            Some(binds[i - 1].as_str())
        };
        let expr = emit_leaf_get(graph, leaf, "cursor_0", focus)?;
        let bind = format!("_c{i}");
        let mut_kw = if needs_mut && (i < chain.len() - 1 || i == 0) {
            "mut "
        } else {
            ""
        };
        out.push_str(&format!("        let {mut_kw}{bind} = {expr};\n"));
        binds.push(bind);
    }
    let result_bind = binds
        .last()
        .ok_or_else(|| "compose map chain must be non-empty".to_string())?;
    debug_assert!(!binds.is_empty(), "binds collected for compose map chain");
    let body_rust = emit_map_body(map_body, map_param, result_bind)?;
    out.push_str(&format!("        let _updated = {body_rust};\n"));

    let mut value_bind = "_updated".to_string();
    for i in (1..chain.len()).rev() {
        let state_bind = &binds[i - 1];
        if is_focus_relative_put(graph, chain[i]) {
            let field = focus_put_field(graph, chain[i])?;
            out.push_str(&format!("        {state_bind}.{field} = {value_bind};\n"));
            value_bind = state_bind.clone();
        } else {
            let out_bind = format!("_p{i}");
            let expr = emit_leaf_put_value(graph, chain[i], state_bind, &value_bind)?;
            out.push_str(&format!("        let {out_bind} = {expr};\n"));
            emit_leaf_put_stores(graph, chain[i], "cursor_0", &out_bind, out)?;
            value_bind = out_bind;
        }
    }
    emit_leaf_put_stores(graph, chain[0], "cursor_0", &value_bind, out)?;
    out.push_str("    }\n}\n\n");
    Ok(())
}

fn fused_provenance_names(
    graph: &CgirGraph,
    original_ids: &[NodeId],
    fallback: &[String],
) -> String {
    let mut names = vec![];
    for &id in original_ids {
        if let Some(
            CgirNode::OpticLeaf { name, .. }
            | CgirNode::PrismLeaf { name, .. }
            | CgirNode::TraversalLeaf { name, .. },
        ) = graph.nodes.get(id as usize)
        {
            if !names.contains(name) {
                names.push(name.clone());
            }
        }
    }
    if names.is_empty() {
        fallback.join(", ")
    } else {
        names.join(", ")
    }
}

fn compose_primary_field(graph: &CgirGraph, entry_leaf: NodeId) -> Result<String, String> {
    let summary = leaf_summary(graph, entry_leaf)?;
    let reg = summary
        .get_reads
        .first()
        .ok_or_else(|| "compose lhs has no read region".to_string())?;
    lookup_region_field(&graph.region_map, reg)
}

fn is_focus_relative_put(graph: &CgirGraph, leaf_id: NodeId) -> bool {
    let Ok(summary) = leaf_summary(graph, leaf_id) else {
        return false;
    };
    summary
        .put_writes
        .iter()
        .any(|r| !graph.region_map.is_top_level_column(r))
}

fn focus_put_field(graph: &CgirGraph, leaf_id: NodeId) -> Result<String, String> {
    let summary = leaf_summary(graph, leaf_id)?;
    if summary.put_writes.is_empty() {
        return Err(format!("leaf {leaf_id} has no put_writes"));
    }
    let paths: Vec<String> = summary
        .put_writes
        .iter()
        .map(|reg| {
            let path = graph.region_map.nested_field_path(reg);
            if path.is_empty() {
                reg.clone()
            } else {
                path.join(".")
            }
        })
        .collect();
    if paths.iter().all(|p| p == &paths[0]) {
        Ok(paths[0].clone())
    } else {
        Err(format!(
            "leaf {leaf_id} put_writes disagree on nested assignment path: {paths:?}"
        ))
    }
}

fn emit_leaf_preview(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    focus_bind: Option<&str>,
) -> Result<String, String> {
    let leaf = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?;
    validate_leaf_params(leaf)?;
    let CgirNode::PrismLeaf {
        preview_body,
        preview_param,
        ..
    } = leaf
    else {
        return Err(format!("node {id} not PrismLeaf"));
    };
    let mut body = preview_body.as_ref().clone();
    if let Some(bind) = focus_bind {
        body = optic_hir::substitute_hir_ident(&body, preview_param, bind);
    }
    emit_pure_hir_expr(&body, cursor)
}

fn emit_leaf_get(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    focus_bind: Option<&str>,
) -> Result<String, String> {
    let leaf = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?;
    validate_leaf_params(leaf)?;
    match leaf {
        CgirNode::PrismLeaf { .. } => emit_leaf_preview(graph, id, cursor, focus_bind),
        CgirNode::OpticLeaf {
            get_body,
            get_param,
            ..
        }
        | CgirNode::TraversalLeaf {
            get_body,
            get_param,
            ..
        } => {
            let mut body = get_body.as_ref().clone();
            if let Some(bind) = focus_bind {
                body = optic_hir::substitute_hir_ident(&body, get_param, bind);
            }
            emit_pure_hir_expr(&body, cursor)
        }
        _ => Err(format!(
            "node {id} not OpticLeaf, PrismLeaf, or TraversalLeaf"
        )),
    }
}

fn emit_leaf_put_value(
    graph: &CgirGraph,
    id: NodeId,
    state_bind: &str,
    value_bind: &str,
) -> Result<String, String> {
    let leaf = graph
        .nodes
        .get(id as usize)
        .ok_or_else(|| format!("leaf {id} missing"))?;
    validate_leaf_params(leaf)?;
    let (put_value_body, put_state_param, put_value_param) = match leaf {
        CgirNode::OpticLeaf {
            put_value_body,
            put_state_param,
            put_value_param,
            ..
        } => (
            put_value_body.as_ref(),
            put_state_param.as_deref(),
            put_value_param.as_deref(),
        ),
        CgirNode::TraversalLeaf {
            set_value_body,
            set_state_param,
            set_value_param,
            ..
        } => (
            set_value_body.as_ref(),
            set_state_param.as_deref(),
            set_value_param.as_deref(),
        ),
        CgirNode::PrismLeaf {
            review_value_body,
            review_state_param,
            review_value_param,
            ..
        } => (
            review_value_body.as_ref(),
            review_state_param.as_deref(),
            review_value_param.as_deref(),
        ),
        _ => {
            return Err(format!(
                "node {id} not OpticLeaf, PrismLeaf, or TraversalLeaf"
            ))
        }
    };
    let Some(body) = put_value_body else {
        return Ok(value_bind.to_string());
    };
    let state = put_state_param.unwrap_or("x");
    let value = put_value_param.unwrap_or("v");
    let subbed = substitute_hir_vars(body.as_ref(), &[(state, state_bind), (value, value_bind)]);
    emit_pure_hir_expr(&subbed, "_unused")
}

fn emit_leaf_put_stores(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    value_bind: &str,
    out: &mut String,
) -> Result<(), String> {
    emit_leaf_review_stores(graph, id, cursor, value_bind, out, "        ")
}

fn emit_leaf_review_stores(
    graph: &CgirGraph,
    id: NodeId,
    cursor: &str,
    value_bind: &str,
    out: &mut String,
    indent: &str,
) -> Result<(), String> {
    let summary = leaf_summary(graph, id)?;
    let regions: Vec<_> = if summary.put_writes.is_empty() {
        summary
            .get_reads
            .first()
            .map(|r| vec![r.clone()])
            .unwrap_or_default()
    } else {
        summary.put_writes.clone()
    };
    if regions.is_empty() {
        return Ok(());
    }
    let mut stores = 0usize;
    for reg in &regions {
        if !graph.region_map.is_top_level_column(reg) {
            continue;
        }
        let field = lookup_region_field(&graph.region_map, reg)?;
        validate_user_ident(&field, "region field")?;
        out.push_str(&format!(
            "{indent}{cursor}.arena.{field}[{cursor}.id] = {value_bind};\n"
        ));
        stores += 1;
    }
    if stores == 0 {
        return Err(format!(
            "leaf put_writes {:?} produced no top-level arena stores",
            summary.put_writes
        ));
    }
    Ok(())
}

fn detect_query_mode(graph: &CgirGraph) -> Result<QueryMode, String> {
    let has_product = graph
        .nodes
        .iter()
        .any(|n| matches!(n, CgirNode::Product { .. } | CgirNode::ProductFlat { .. }));

    if graph.roots.is_empty() {
        return Err("CGIR graph has no query root".into());
    }

    for &root in &graph.roots {
        if let Some(node) = graph.nodes.get(root as usize) {
            return Ok(match node {
                CgirNode::FusedLoop { compose_body, .. } if compose_body.is_some() => {
                    QueryMode::FusedCompose { root }
                }
                CgirNode::QueryGet { .. } => QueryMode::Get { root },
                CgirNode::QuerySet { .. } => QueryMode::Set { root },
                CgirNode::QueryMap { optic_name, .. } => {
                    if let Some(&nid) = graph.resolved_optics.get(optic_name) {
                        if matches!(
                            graph.nodes.get(nid as usize),
                            Some(CgirNode::Compose { .. })
                        ) {
                            return Ok(QueryMode::MapCompose {
                                root,
                                compose_id: nid,
                            });
                        }
                    }
                    if has_product {
                        QueryMode::MapProduct { root }
                    } else {
                        QueryMode::MapDecay { root }
                    }
                }
                other => {
                    return Err(format!("unsupported CGIR root node for codegen: {other:?}"));
                }
            });
        }
    }
    Err(format!(
        "CGIR root {:?} is out of bounds (nodes={})",
        graph.roots,
        graph.nodes.len()
    ))
}

